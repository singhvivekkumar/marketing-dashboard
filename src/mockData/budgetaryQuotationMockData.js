/**
 * Get mock data for initial load testing
 * @returns {Object} Mock BQ data
 */
export const getMockBudgetaryQuotationData = () => {
  return mockBudgetaryQuotationData;
};

/**
 * Get single BQ record by ID
 * @param {string} id - BQ record ID
 * @returns {Object} Single BQ record or null
 */
export const getMockBQById = (id) => {
  return mockBudgetaryQuotationData.data.find((record) => record.id === id) || null;
};

/**
 * Update mock data (simulates backend update)
 * @param {string} id - BQ record ID
 * @param {Object} updates - Fields to update
 * @returns {Object} Updated record
 */
export const updateMockBQ = (id, updates) => {
  const index = mockBudgetaryQuotationData.data.findIndex((r) => r.id === id);
  if (index === -1) return null;

  mockBudgetaryQuotationData.data[index] = {
    ...mockBudgetaryQuotationData.data[index],
    ...updates,
  };
  return mockBudgetaryQuotationData.data[index];
};

/**
 * Delete mock data (simulates backend delete)
 * @param {string} id - BQ record ID
 * @returns {boolean} Success status
 */
export const deleteMockBQ = (id) => {
  const index = mockBudgetaryQuotationData.data.findIndex((r) => r.id === id);
  if (index === -1) return false;

  mockBudgetaryQuotationData.data.splice(index, 1);
  return true;
};

/**
 * Add new mock BQ record
 * @param {Object} newRecord - New BQ record data
 * @returns {Object} Added record with ID
 */
export const addMockBQ = (newRecord) => {
  const id = `BQ${String(mockBudgetaryQuotationData.data.length + 1).padStart(3, "0")}`;
  const record = {
    id,
    dateCreated: new Date().toISOString().split("T")[0],
    ...newRecord,
  };
  mockBudgetaryQuotationData.data.push(record);
  return record;
};

/**
 * Filter mock data by status
 * @param {string} status - Status to filter
 * @returns {Array} Filtered records
 */
export const filterMockBQByStatus = (status) => {
  return mockBudgetaryQuotationData.data.filter(
    (record) => record.presentStatus === status
  );
};

/**
 * Filter mock data by defence type
 * @param {string} type - Defence type (Defence, Non-Defence, Civil)
 * @returns {Array} Filtered records
 */
export const filterMockBQByDefenceType = (type) => {
  return mockBudgetaryQuotationData.data.filter(
    (record) => record.defenceAndNonDefence === type
  );
};

